#A Data Structure is a way of organizing, storing, and managing data so that it can be used efficiently.
# Main built in data structures are:
# list
# tuple
# set
# dictionary(dict)

#1.(list) Stores multiple items in one variable
# example: 
fruits = ['apple', 'banana', 'mango', 'orange', 'banana', 'apple']
print(fruits)

#2.(tuple) Similar to a list, but cannot be changed
# example: 
points = (10,30)
print(points)

#3.(set) Stores unique items only (no duplicates)
# example:
colors = {'red', 'blue', 'green'}
print(colors)

#4.Dictionary(dict) Stores data as key and value
# example: 
student = {'name': 'Kaif', 'age': 18}
print(student)